<div>
	<h1>함수달 개인정보보호정책 및 개인정보처리방침</h1>

	<p>
		함수달은 이용자의 개인정보를 소중히 여기며, 개인정보 보호와 관련하여 「정보통신망 이용촉진 및
		정보보호 등에 관한 법률」 및 「개인정보 보호법」 등 관련 법령상의 개인정보 보호 규정을 철저히
		준수하고 있습니다.
	</p>

	<p>
		함수달은 함수달 행사의 개인정보처리방침을 통해 이용자의 개인정보가 남용되거나 유출되지 않도록
		최선을 다할 것이며, 이용자가 제공하는 개인정보가 어떤 용도와 방식으로 이용되고 있는지, 그리고
		개인정보보호를 위해 어떤 조치가 이루어지고 있는지 안내드립니다.
	</p>

	<p>
		단, 본 개인정보처리방침은 정부의 법령 및 지침 변경, 또는 더 나은 서비스 제공을 위해 내용이
		변경될 수 있으니, 이용자는 함수달 인스타그램 방문 시 수시로 내용을 확인해 주시기 바랍니다.
		함수달은 개인정보처리방침을 개정하는 경우, 웹사이트 공지사항 또는 이메일을 통해 사전에 공지할
		것입니다.
	</p>

	<section>
		<h2>✔ 개인정보 수집 항목 및 이용 목적</h2>
		<p>
			서비스 운영과 참가자 지원을 위해 필요한 최소한의 개인정보를 수집합니다. 행사 등록, 진행, 사후
			커뮤니티 안내 등의 과정에서 함수달 웹사이트, 이메일, Google Forms 등을 통해 원활한 행사 운영을
			위한 참가자의 개인정보를 수집하며, 수집 항목 및 이용 목적은 다음과 같습니다.
		</p>

		<h3>1. 수집 항목</h3>
		<ul>
			<li>성명</li>
			<li>이메일</li>
			<li>휴대폰 번호</li>
		</ul>

		<h3>2. 수집 / 이용 목적</h3>
		<ul>
			<li>
				<strong>서비스 제공</strong><br />세미나의 원활한 진행을 목적으로 개인정보를 처리합니다.
				참가자에게 세미나 정보, 참여 안내, 사후 커뮤니티 초대 등 본 서비스를 제공하기 위해
				사용됩니다.
			</li>
			<li>
				<strong>서비스 분석과 개선</strong><br />참가자의 세미나 참여 통계 및 피드백을 통해 서비스
				품질 개선에 활용됩니다.
			</li>
			<li>
				<strong>홍보</strong><br />세미나 운영, 홍보 및 참가자 관리 등을 위한 목적으로 일부
				개인정보가 이용됩니다. 사전 개인정보 지침 관련 별도 동의 하에, 참가자들의 참여 활동 내역(예:
				세미나 인증 사진) 및 관련 미디어 자료는 함수달의 홍보 수단으로 사용될 수 있습니다.
			</li>
		</ul>
	</section>

	<section>
		<h2>✔ 개인정보 처리</h2>
		<p>
			특별한 경우(참가자의 명시적 동의가 있는 경우)를 제외하고, 서비스의 원활한 운영 이외의 목적으로
			제3자에게 참가자의 개인정보를 제공하지 않습니다.
		</p>
	</section>

	<section>
		<h2>✔ 개인정보 파기</h2>
		<p>
			세미나 진행 종료 후 개인정보의 보유 기간 내에서 필요한 개인정보 처리 목적이 달성된 경우, 지체
			없이 해당 개인정보를 파기합니다.
		</p>

		<h3>파기 절차</h3>
		<p>
			참가자가 제공한 정보는 목적 달성 후 별도의 DB로 옮겨져(종이의 경우 별도의 서류로 보관) 내부
			방침 및 관련 법령에 따라 일정 기간 저장된 후 파기됩니다. 이 과정에서 DB로 옮겨진 개인정보는
			법률에 의한 경우를 제외하고 다른 목적으로 이용되지 않습니다.
		</p>

		<h3>파기 기한</h3>
		<p>
			참가자의 개인정보는 보유 기간인 세미나 종료 후 1개월이 경과한 경우, 개인정보의 처리 목적 달성
			후 해당 개인정보를 파기합니다.
